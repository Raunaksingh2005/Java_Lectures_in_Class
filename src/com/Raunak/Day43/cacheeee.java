package com.Raunak.Day43;
import java.util.*;
public class cacheeee {
    public static void main(String[] args){
        Scanner sc=new Scanner(System.in);
        int n= sc.nextInt();
        String[] cache=new String[n];
        for(int i=0;i<n;i++){
            cache[i]=sc.next();
        }
        for(int i=0;i<n;i++){
            System.out.println(cache[i]);
        }
    }
}
